size_t Constituent::constituentIndex(const std::string &name) {
  auto i = std::find(s_names.begin(), s_names.end(), name);
  if (i != s_names.end()) {
    return i - s_names.begin();
  } else {
    return Constituent::nullvalue<size_t>();
  }
}

